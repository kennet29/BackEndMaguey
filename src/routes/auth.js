const express = require('express');
const router = express.Router();
const jwt = require('jsonwebtoken');
require('dotenv').config();

const secretKey = process.env.SECRET_KEY;

// Funci칩n para crear un token JWT
function createToken(user) {
  const payload = {
    username: user.username,
  };

  const token = jwt.sign(payload, secretKey, { expiresIn: '5m' });
  return token;
}

function authenticateToken(req, res, next) {
  const token = req.headers['authorization'];
  if (!token) return res.status(401).json({ message: 'Token no proporcionado' });

  try {
    const decoded = jwt.verify(token, secretKey);
    req.user = decoded;
    next();
  } catch (err) {
    res.status(403).json({ message: 'Token no v치lido' });
  }
}

// Ruta de inicio de sesi칩n
router.post('/login', (req, res) => {
  const { username, password } = req.body;
  // Verifica las credenciales del usuario, por ejemplo, con una base de datos
  if (username === 'usuario' && password === 'contrase침a') {
    const token = createToken({ username });
    res.json({ token });
  } else {
    res.status(401).json({ message: 'Credenciales incorrectas' });
  }
});

// Ruta protegida
router.get('/ruta-protegida', authenticateToken, (req, res) => {
  res.json({ message: 'Acceso permitido' });
});

module.exports = router;
